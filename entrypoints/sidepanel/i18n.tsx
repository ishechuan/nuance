type LoadingState = {
  readonly extracting: string;
  readonly sending: string;
  readonly analyzing: string;
  readonly processing: string;
};

type DictKey = keyof typeof dict.en;
type DictValue = string | ((arg1: string, arg2?: string) => string) | LoadingState;

interface I18nContextValue {
  lang: Lang;
  t: (key: DictKey, arg1?: string, arg2?: string) => string;
  tLoadingState: () => LoadingState;
  setLang: (lang: Lang) => void;
}

const I18nContext = createContext<I18nContextValue>({
  lang: 'en',
  t: (k, arg1, arg2) => {
    const value = dict.en[k];
    if (typeof value === 'function') {
      return value(arg1 || '', arg2 || '');
    }
    if (typeof value === 'string') {
      return value;
    }
    return k;
  },
  tLoadingState: () => dict.en.loadingState,
  setLang: () => {},
});

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>('en');

  useEffect(() => {
    getSettings().then((s) => {
      setLangState(s.language);
    });
  }, []);

  useEffect(() => {
    document.documentElement.lang = lang;
  }, [lang]);

  const setLang = (next: Lang) => {
    setLangState(next);
    setSettings({ language: next });
  };

  const t = useMemo(() => {
    const table = dict[lang];
    return (key: DictKey, arg1?: string, arg2?: string) => {
      const value = table[key];
      if (typeof value === 'function') {
        return value(arg1 || '', arg2 || '');
      }
      if (typeof value === 'string') {
        return value;
      }
      return key;
    };
  }, [lang]);

  const tLoadingState = useMemo(() => {
    const table = dict[lang];
    return () => table.loadingState;
  }, [lang]);

  const value = useMemo(
    () => ({ lang, t, tLoadingState, setLang }),
    [lang, t, tLoadingState]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}
